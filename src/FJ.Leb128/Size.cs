namespace FJ.Leb128;

/// <summary>
/// Represents the size of an integer in bits.
/// </summary>
public enum Size
{
    /// <summary>
    /// Represents an 8-bit integer size.
    /// </summary>
    Bits8,

    /// <summary>
    /// Represents a 32-bit integer size.
    /// </summary>
    Bits32,

    /// <summary>
    /// Represents a 64-bit integer size.
    /// </summary>
    Bits64,

    /// <summary>
    /// Represents a 128-bit integer size.
    /// </summary>
    Bits128
}
